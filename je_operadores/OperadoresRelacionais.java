package je_operadores;

public class OperadoresRelacionais {
    public static void main(String[] args){

        /*
        == Igual
        < Menor
        > Maior
        <= Menor ou Igual
        >= Maior ou Igual
        != Diferente
        */

        int valor1 = 20;
        int valor2 = 10;

        System.out.println(valor1 == valor2);
        System.out.println(valor1 > valor2);
        System.out.println(valor1 < valor2);
        System.out.println(valor1 >= valor2);
        System.out.println(valor1 <= valor2);
        System.out.println(valor1 != valor2);

    }
}
